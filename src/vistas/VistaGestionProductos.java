    public void modelarTabla(){
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Categoria");
        modeloTabla.addColumn("Precio");
        jtaProductos.setModel(modeloTabla);
    }
    public void datosTabla(ArrayList<Producto> producto){
        modeloTabla.getDataVector().removeAllElements();
        for(Producto i:producto){
            modeloTabla.addRow(new Object[]{i.getNombre(),i.getCategoria(), "$"+i.getPrecio()});
        }
    }

