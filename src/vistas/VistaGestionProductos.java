    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        // TODO add your handling code here:
        try{
            int precio=Integer.parseInt(jtPrecio.getText());
            String nombre=jtNombre.getText();
            String categoria=(String) jcCategoria.getSelectedItem();
            if(!nombre.isEmpty()){
            Producto p = new Producto(nombre,categoria,precio);
            listaProducto.add(p);
            datosTabla(listaProducto);
            JOptionPane.showMessageDialog(this, "¡Producto agregado a la tabla!", "Producto guardado", JOptionPane.INFORMATION_MESSAGE);
            jtPrecio.setText("");
            jtNombre.setText("");
            }else{
             JOptionPane.showMessageDialog(this, "Los campos de texto estan vacios", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Precio solo acepta valores numericos", "Error en el precio", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbAgregarActionPerformed

